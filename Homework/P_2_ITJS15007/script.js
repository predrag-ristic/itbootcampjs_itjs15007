// Randomizacija
function randomizacija(niz)
{
    for (let i = niz.length - 1; i > 0; i--)
    {
        let j = Math.floor(Math.random() * (i + 1));
        [niz[i], niz[j]] = [niz[j], niz[i]];
    }

}
